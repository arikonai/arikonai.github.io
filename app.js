const views = ['home', 'about', 'privacy', 'terms', 'cookies'];

function showView(view) {
  views.forEach(v => {
    const el = document.getElementById('view-' + v);
    if (el) el.style.display = (v === view) ? '' : 'none';
  });
  window.scrollTo(0, 0);

  // Trigger reveal animations for new view
  setTimeout(initReveal, 50);
}

/* -----------------------------------------
   NAVBAR SCROLL
   ----------------------------------------- */
function initNavbar() {
  const navbar = document.getElementById('navbar');
  window.addEventListener('scroll', () => {
    if (window.scrollY > 20) {
      navbar.classList.add('scrolled');
    } else {
      navbar.classList.remove('scrolled');
    }
  });
}

/* -----------------------------------------
   MOBILE MENU
   ----------------------------------------- */
function toggleMobileMenu() {
  const menu = document.getElementById('mobileMenu');
  const btn = document.getElementById('menuIcon');
  const isOpen = menu.style.display !== 'none';
  menu.style.display = isOpen ? 'none' : 'flex';
  btn.innerHTML = isOpen ? '&#9776;' : '&#x2715;';
}

function closeMobileMenu() {
  document.getElementById('mobileMenu').style.display = 'none';
  document.getElementById('menuIcon').innerHTML = '&#9776;';
}

/* -----------------------------------------
   PLATFORM DROPDOWN
   ----------------------------------------- */
function togglePlatform(e) {
  e.stopPropagation();
  const menu = document.getElementById('platformMenu');
  const chevron = document.getElementById('platformChevron');
  const isOpen = menu.classList.contains('open');
  menu.classList.toggle('open', !isOpen);
  chevron.style.transform = isOpen ? '' : 'rotate(180deg)';
}

document.addEventListener('click', () => {
  const menu = document.getElementById('platformMenu');
  if (menu) menu.classList.remove('open');
  const chevron = document.getElementById('platformChevron');
  if (chevron) chevron.style.transform = '';
});

/* -----------------------------------------
   REVEAL ON SCROLL (Intersection Observer)
   ----------------------------------------- */
function initReveal() {
  const targets = document.querySelectorAll('.reveal, .fade-up');
  if (!targets.length) return;

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry, i) => {
      if (entry.isIntersecting) {
        // Stagger delay based on position among siblings
        const siblings = Array.from(entry.target.parentElement.children);
        const idx = siblings.indexOf(entry.target);
        const delay = Math.min(idx * 80, 400);
        setTimeout(() => {
          entry.target.classList.add('revealed');
        }, delay);
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.1, rootMargin: '0px 0px -40px 0px' });

  targets.forEach(el => observer.observe(el));
}

